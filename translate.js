// ======= MOTEUR DE TRADUCTION =======
let currentLang = 'fr';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('cafeZizLang', lang); // Save user preference
    const t = translations[lang];

    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key] !== undefined) {
            if (el.hasAttribute('data-i18n-html')) {
                el.innerHTML = t[key];
            } else {
                el.textContent = t[key];
            }
        }
    });

    // Handle RTL for Arabic
    const menuBorder = document.querySelector('.menu-border');
    const html = document.documentElement;
    if (lang === 'ar') {
        html.setAttribute('dir', 'rtl');
        html.setAttribute('lang', 'ar');
        menuBorder.style.direction = 'rtl';
    } else {
        html.setAttribute('dir', 'ltr');
        html.setAttribute('lang', lang === 'en' ? 'en' : 'fr');
        menuBorder.style.direction = 'ltr';
    }

    // Update active button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
    // Restore language from previous visit if available
    const savedLang = localStorage.getItem('cafeZizLang');
    if (savedLang) {
        setLanguage(savedLang);
    }

    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            setLanguage(btn.getAttribute('data-lang'));
        });
    });
});
